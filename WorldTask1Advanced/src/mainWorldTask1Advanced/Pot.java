package mainWorldTask1Advanced;

public class Pot {

	double x = 0.0;
	double y = 0.0;
	
	public Pot(double x, double y){
		this.x = x;
		this.y = y;
	}
	
	public void setX(double x){
		this.x = x;
	}
	
	public void setY(double y){
		this.y = y;
	}
	
	public double getX(){
		return x;
	}
	
	public double getY(){
		return y;
	}
}
